export const getTranslation = (lang: string = 'en') => {
  const code = (lang || 'en').toLowerCase().slice(0, 2) as SupportedLanguage;
  return translations[code] || translations.en;
};

export const translateClinical = (text: string = '', lang: string = 'en') => {
  if (!text) return '';
  const t = getTranslation(lang);
  const normalized = text.toLowerCase().trim();
  
  if (normalized.includes('cardiol')) return (t as any).cardiology;
  if (normalized.includes('neur')) return (t as any).neurology;
  if (normalized.includes('ortho')) return (t as any).orthopedics;
  if (normalized.includes('pedia')) return (t as any).pediatrics;
  if (normalized.includes('general') || normalized.includes('medicine')) return (t as any).generalMedicine;
  if (normalized.includes('urol')) return (t as any).urology;
  
  // Handle Doctor Title Shortening (Fixing "డాక్టర్" -> "డా.")
  let result = text;
  if (lang === 'te') result = result.replace(/డాక్టర్/g, 'డా.');
  if (lang === 'hi') result = result.replace(/डॉक्टर/g, 'डॉ.');
  if (lang === 'mr') result = result.replace(/डॉक्टर/g, 'डॉ.');
  if (lang === 'ta') result = result.replace(/டாக்டர்/g, 'டாக்டர்.'); // Tamil usually keeps longer or use 'டாக்டர்'
  
  return result; // Fallback
};
